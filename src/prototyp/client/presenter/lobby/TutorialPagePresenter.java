package prototyp.client.presenter.lobby;

import prototyp.client.presenter.PagePresenter;
import prototyp.client.view.lobby.TutorialPage;

import com.smartgwt.client.widgets.Canvas;

/**
 * Der Presenter zur TutorialPage
 * 
 * @author Andreas (Verantwortlicher)
 * @version 1.0
 * 
 * @see TutorialPage
 */
public class TutorialPagePresenter implements PagePresenter {

	/** Zugehörige Page */
	private TutorialPage page;

	/** Konstruktor */
	public TutorialPagePresenter() {
		this.page = new TutorialPage();
	}

	/**
	 * Liefert die zugehörige Page
	 * 
	 * @return TutorialPage
	 */
	@Override
	public Canvas getPage() {
		return this.page;
	}

}
