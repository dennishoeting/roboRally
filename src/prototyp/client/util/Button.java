package prototyp.client.util;

import com.smartgwt.client.widgets.IButton;

/**
 * 
 * Erweiterung von IButton
 * 
 * @author Dennis HÃ¶ting
 * @version 1.0
 * @see IButton
 */
public class Button extends IButton {

	/**
	 * Konstruktor, AutoFit=true
	 * 
	 * @param title
	 *            Beschriftung des Buttons
	 */
	public Button(String title) {
		super(title);
		setAutoFit(true);
	}

	/**
	 * Konstruktor, AutoFit=false
	 * 
	 * @param title
	 *            Beschriftung des Buttons
	 * @param width
	 *            Breite des Buttons
	 */
	public Button(String title, int width) {
		this(title);
		setAutoFit(false);
		this.setWidth(width);
	}
	
	/**
	 * Konstruktor, AutoFit=false
	 * 
	 * @param title
	 *            Beschriftung des Buttons
	 * @param width
	 *            Breite des Buttons
	 */
	public Button(String title, int width, int height) {
		this(title);
		setAutoFit(false);
		this.setWidth(width);
		this.setHeight(height);
	}
	
	/**
	 * Konstruktor, AutoFit=false
	 * 
	 * @param title
	 *            Beschriftung des Buttons
	 * @param width
	 *            Breite des Buttons
	 */
	public Button(int width, int height) {
		setAutoFit(false);
		this.setWidth(width);
		this.setHeight(height);
	}
}
