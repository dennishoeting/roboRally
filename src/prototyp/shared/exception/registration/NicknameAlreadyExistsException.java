package prototyp.shared.exception.registration;

import prototyp.client.view.Page;
import prototyp.server.view.AdministrationImpl;
import prototyp.shared.exception.RoboException;

/**
 * NickNameAlreadyExitsException - Wird geworfen, wenn der Nickname schon
 * vorhanden ist.
 * 
 * @author Andreas
 * @version 1.0
 * 
 * @see AdministrationImpl
 */
public class NicknameAlreadyExistsException extends RoboException {

	/** Seriennummer */
	private static final long serialVersionUID = 6378706769584215861L;

	/**
	 * Konstruktor
	 */
	public NicknameAlreadyExistsException() {

	}

	@Override
	public String getMessage() {
		return Page.props.exception_nicknameAlreadyExitsException();
	}
}
