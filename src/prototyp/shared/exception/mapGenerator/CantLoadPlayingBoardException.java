package prototyp.shared.exception.mapGenerator;

import prototyp.client.view.Page;
import prototyp.shared.exception.RoboException;

/**
 * Wird geworfen wenn ein PlayingBoard nicht geladen werden kann.
 * 
 * @author timo
 * @version 1.0
 */
public class CantLoadPlayingBoardException extends RoboException {

	/**
	 * Seriennummer
	 */
	private static final long serialVersionUID = 8531631622149195834L;

	/**
	 * Default
	 */
	public CantLoadPlayingBoardException() {

	}

	/**
	 * Liefert die Fehlernachricht
	 * 
	 * @return Fehlernachricht
	 */
	@Override
	public String getMessage() {
		return Page.props.exception_mapgenerator_cantLoadPlayingBoardException();
	}
}
