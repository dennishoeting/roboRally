package prototyp.shared.exception.playingboard;

/**
 * Exception die geworfen wird, wenn eine Wand auf einem Spielfeld nicht
 * existiert, diese aber entfernt werden soll.
 * 
 * @author Marcus
 * 
 */
public class WallNotExistsException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8165889086177666476L;

	/**
	 * Default-Konstruktor
	 */
	public WallNotExistsException() {
	}

	/**
	 * Konstruktor
	 * 
	 * @param message
	 *            der Fehlertext
	 */
	public WallNotExistsException(String message) {
		super(message);
	}
}
