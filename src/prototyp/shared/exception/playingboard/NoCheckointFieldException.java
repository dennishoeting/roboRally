package prototyp.shared.exception.playingboard;

import prototyp.client.view.Page;

/**
 * Wird geworfen, wenn sich kein Checkpointfeld auf dem Spielbrett befindet
 * 
 * @author Marcus
 * @version 1.0
 */
public class NoCheckointFieldException extends PlayingboardException {
	/**
	 * Seriennummer
	 */
	private static final long serialVersionUID = 8232678974980988050L;

	public NoCheckointFieldException() {
	}

	@Override
	public String getMessage() {
		return Page.props.exception_playingboard_noCheckointFieldException();
	}

}
