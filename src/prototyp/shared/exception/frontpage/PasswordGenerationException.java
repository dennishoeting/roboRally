package prototyp.shared.exception.frontpage;

import prototyp.client.view.Page;
import prototyp.shared.exception.RoboException;

/**
 * PasswordGenerationException - Wird geworfen, wenn das Passwort nicht
 * generiert werden kann.
 * 
 * @author Kamil
 * @version 1.0
 * 
 * @see LobbyImpl, FrontPageImpl
 */
public class PasswordGenerationException extends RoboException {

	/** Seriennummer */
	private static final long serialVersionUID = -5810310991546462952L;

	/**
	 * Konstruktor
	 */
	public PasswordGenerationException() {
	
	}

	@Override
	public String getMessage() {
		return Page.props.exception_passwordGenerationException();
	}
}
