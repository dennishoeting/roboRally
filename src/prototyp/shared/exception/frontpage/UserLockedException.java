package prototyp.shared.exception.frontpage;

import prototyp.client.view.Page;
import prototyp.shared.exception.RoboException;

/**
 * UserLockedException - Wird geworfen wenn ein gesperrter User versucht sich
 * einzuloggen
 * 
 * @author Kamil
 * @version 1.0
 */
public class UserLockedException extends RoboException {

	/**
	 * Seriennummer
	 */
	private static final long serialVersionUID = 1788913777823124189L;

	/**
	 * Default
	 */
	public UserLockedException() {

	}

	@Override
	public String getMessage() {
		return Page.props.exception_userLockedException();
	}
}
